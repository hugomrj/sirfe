package py.com.aplicacion.decreto;





public class Decreto {
    
    private Integer agno;
    private Integer decreto;
    private String programa;
    private String subprograma;

    public Integer getAgno() {
        return agno;
    }

    public void setAgno(Integer agno) {
        this.agno = agno;
    }

    public Integer getDecreto() {
        return decreto;
    }

    public void setDecreto(Integer decreto) {
        this.decreto = decreto;
    }

    public String getPrograma() {
        return programa;
    }

    public void setPrograma(String programa) {
        this.programa = programa;
    }

    public String getSubprograma() {
        return subprograma;
    }

    public void setSubprograma(String subprograma) {
        this.subprograma = subprograma;
    }
    
    
    
}


