package py.com.aplicacion.objeto_gasto;

/*
 * @author hugo
 */
public class ObjetoGasto {
    
    private Integer objeto;
    private String descripcion;
    private String imputa;

    public Integer getObjeto() {
        return objeto;
    }

    public void setObjeto(Integer objeto) {
        this.objeto = objeto;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getImputa() {
        return imputa;
    }

    public void setImputa(String imputa) {
        this.imputa = imputa;
    }
    
}
